#!/usr/bin/env python
"""
Load Synthetic Data into PostgreSQL

Loads CSV files generated by scripts/generate_synthetic_data.py into a PostgreSQL database.

Features:
- Reads CSV files from data/ directory
- Validates data schema and constraints before insert
- Uses SQLAlchemy for database operations
- Transactional loading (all-or-nothing)
- Detailed logging of insert counts
- Data type validation
- Referential integrity checks
- Safety checks to prevent loading into production

Usage:
    python scripts/load_data.py
    python scripts/load_data.py --db-url postgresql://user:pass@localhost:5432/dbname
    python scripts/load_data.py --data-dir /path/to/data --db-url postgresql://...
    python scripts/load_data.py --db-url postgresql://... --force

Environment:
    DATABASE_URL - PostgreSQL connection URL (fallback)

CSV File Format (Expected):
    - users.csv: id, email, name, phone, date_of_birth, gender, country
    - financial_summary.csv: id, user_id, year, month, total_income, total_expenses, ...
    - transactions.csv: id, user_id, transaction_date, amount, category, merchant_name, ...
    - goals.csv: id, user_id, goal_name, description, target_amount, current_amount, ...
    - holdings.csv: id, user_id, ticker, quantity, average_cost, current_value, ...
    - market_prices.csv: id, ticker, price_date, open_price, close_price, ...

Validation Rules:
    - All user_id references must exist in users table
    - No null values in required columns
    - Amounts must be non-negative
    - Dates must be valid ISO format
    - Categories must match predefined list
"""

